The Tasks side interface
</div>

Clicking the green star icon on the side panel will open the interface pictured to the left. This interface shows you up to six random Tasks that you have yet to complete in the local area. Clicking on a Task here will display more information about it including its name, the set that it belongs to and its difficulty level (both explained in the Task Sets section below), a description of the Task, and sometimes even a hint on how to go about completing it. There will also be some information on what rewards you can gain by completing all Tasks in the set that it belongs to (see Rewards section below).
<br><br>
If you right-click on one of these Tasks you can choose to 'pin' it (a pinned task is denoted with a small + symbol). Pinning a Task like this means that it will always appear in this side interface, regardless of your location in RuneScape. You can change which Task you have pinned at any time, so it's a good way to remind yourself which Task you're currently working towards, among other things.
