<br><br>
All Tasks also have a difficulty level. There are five difficulty levels of Task: Beginner, Easy, Medium, Hard and Elite.
<br>
<ul>
<li>Beginner Tasks should be able to be completed by every player with relative ease.</li>
<li>Easy Tasks might require some quests to be complete, but they will generally be low-level quests. Almost any player should be capable of completing all of the Easy Tasks.</li>
<li>Medium Tasks may require a few more quests, be in more dangerous locations or require several of your skills to be advanced.</li>
<li>Hard Tasks may not appear difficult at first glance, but they may require you to enter incredibly dangerous areas, to have completed high-level quests or to survive through all the horrors an area can throw at you, just to eat a kebab...</li>
<li>Elite Tasks will ask for very high levels (often up in the 90s) or for a series of steps to be completed within a certain area. These will test all but the most experienced players, pushing your knowledge and skills to the extreme. The rewards, however, are more than worth it...</li>
</ul>
<br>
With the exception of the Lumbridge/Draynor set, all sets have Tasks ranging in difficulty from Easy to Elite. The Lumbridge/Draynor set instead has Beginner to Hard Tasks, as it's intended as an introduction to the system.
