The Task List
</div>

At the bottom of the side panel interface is the 'Open Task List' button, which (funnily enough) opens the Task List, shown to the right. Here you are able to view all Tasks (by set) and, should you wish, filter out any that are not part of the current set you are viewing or which you have already completed (using the two buttons at the top-right). Clicking on any Task in the Task List will also display more information about it. You are also able to pin a Task from here as well. Finally, the check-box at the bottom-left of this interface lets you toggle on/off the notifications that pop up in the main game window whenever you complete a Task.
