Completing one of the Karamja Tasks
</div>

The lands of RuneScape are varied and picturesque. Even the fetid swamps of Mort Myre and the blasted wastes of the Wilderness possess their special charms for those who care to look. Of course, the scenery isn't everything, and each land has its specialties: from the mines of Keldagrim to the plants and animals of Karamja. If you're a wee bit bored and fancy a few small challenges, you might like to check out your Tasks to see what the people of those lands do to prove their proficiency in all things.
