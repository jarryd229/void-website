                 <div class="headings"> <span class="headertitleleft"><span class="headertitleright">Introduction</span></span> 
                 </div> 
                 <br> While playing RuneScape you might experience a strange, unexpected event, or notice something peculiar taking place nearby. These random events will typically involve the sudden appearance of an unusual animal or person, who will ask you to perform a quick task for them or solve a puzzle. Completing the event won't take long, and if you're lucky you may well profit from it! Make sure you keep your wits about you, though, because random events can happen at almost any time and in almost any place. 
                 <br><a name="How_random_events_work"></a>
                 <br class="clear"> 
                 <div class="headings"> <span class="headertitleleft"><span class="headertitleright">How random events work</span></span> 
                 </div> 
                 <br> Random events will teleport you to unusual locations that may or may not be a part of RuneScape itself. You will not be able to leave until you either successfully complete the random event, or fail in the attempt. If you successfully complete the random event you will be returned to the spot you were taken from; if you fail, you will be teleported to a random location. Don't worry too much about failing an event, though: you will never be teleported to a dangerous location. 
                 <br>
                 <br> Random events will occur with a certain degree of regularity, but if you consistently complete events successfully, you will encounter them less frequently. 
