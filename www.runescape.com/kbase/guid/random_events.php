                 <br><a name="Rewards"></a>
                 <br class="clear"> 
                 <div class="headings"> <span class="headertitleleft"><span class="headertitleright">Rewards</span></span> 
                 </div> 
                 <br> Whenever you successfully complete a random event, you will be given a random event gift box. (The only exception to this is the Maze event.) Inside the box will be a selection of items, from which you can choose one reward. Gift boxes will always contain a selection of coins, runes, coal, essence, ore, bars, gems, herbs, seeds, charms and experience lamps for you to choose from. In addition to this, some gift boxes will give you the choice of costume points, which can be exchanged for part of an outfit with Iffie at Thessalia's clothes shop in Varrock. 
                 <br><a name="Retired_random_events"></a>
                 <br class="clear"> 
                 <div class="headings"> <span class="headertitleleft"><span class="headertitleright">Retired random events</span></span> 
                 </div> 
                 <br> Some random events are no longer active, which in most cases is because the person (or creature) responsible was considered to no longer be up to the job. Most of the guardians, for example, now spend their days in the Enchanted Valley (which you can get to using the fairy rings), while some of the more unsavoury types, like Cap'n Hand, reside in Port Sarim jail. You'll still see Dr Jekyll from time to time, dashing to the Varrock apothecary in a desperate bid to keep Mr Hyde in check; and if you're in Karamja to the south-west of Brimhaven, you may come across the strange plant, which still produces rather excellent fruit. The Drunken Dwarf will also be seen on occasion, staggering about in an erratic fashion. If you talk to him, and he takes a liking to you, he might treat you to a kebab and some beer. 
