To begin completing tasks in your Achievement Diary, talk to one of the NPCs named at the top of the scroll.
<br><br>
There are three categories of task: Easy, Medium and Hard.
<br><br>
Easy tasks might require some quests to be complete, but they will generally be low level quests. Almost any player should be capable of completing all of the easy tasks.
<br><br>
Medium tasks may require a few more quests, be in more dangerous locations, or require several of your skills to be advanced.
<br><br>
Hard tasks may not appear difficult on first glance, but they may require you to enter incredibly dangerous areas, to have completed Master Quests, or to survive through all the horrors an area can throw at you, just to eat a kebab...
