Each time you complete a set of tasks (easy, medium or hard), you will receive a message in your chat box congratulating you and reminding you who you should speak to in order to collect your reward.
<br><br>
Each area and level has a different reward, and the rewards will increase each level. Often, these rewards will allow you greater freedom in the area or provide you with new training areas restricted to those who have completed the tasks.
<br><br>
So, rather than picking another inventory of flax, perhaps you'd prefer to explore the delights of the fair world of RuneScape?
<br><br>
<b>Note</b> that the experience lamps you receive can be used on any skill, provided you have at least level 30 (for an easy reward lamp), level 40 (for a medium reward lamp) or level 50 (for a hard reward lamp).
