Random handicap
</div>

<p>For this type of battle you must move up the floors, fighting a random boss at every level. You will also face a random handicap at each level which is stacked with the handicaps from the previous levels. Each level is a gamble of your reward; if you are defeated your reward is lessened. Handicaps are a way of making the battle harder such as reducing your magic attacks or starting the battle on half health.</p>
<p>The handicaps can be nudged or re-spun, up to three times. Re-spin will start the wheel of handicaps again, allowing you another chance to stop the wheel at a more favourable handicap. Nudge will move the wheel down to the next handicap that you haven't encountered.</p>
<p>If you do not want to risk your progress (press 'hold on' when your next fight is shown to you if you are about to enter a fight), claim your reward from the reward box. Doing so will reset your progress and you will have to start from the beginning if you wish to take part in another climber battle.</p>
<p>Note that once you have started a Climber battle, you will not be able to take part in any of the other battle types until you have finished the battle by claiming your reward.</p>
<p><span class="title">Endurance</span></p>
<p>This mode is the same as Climber only there are no handicaps in this battle type and you cannot use a bank between battles.</p>
