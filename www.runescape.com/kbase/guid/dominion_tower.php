Freestyle battle setup
</div>
</div>
<div class="clear"></div>

<p>With this game mode, you can fight a boss of your choosing using any set-up that you like. Choose a boss from the left hand side and then decide which, if any, handicaps you want to activate for the fight. You can only battle NPCs that you have completed the quest for and that have faced in the Climber and Endurance modes.</p>
<p><span class="title">Special</span></p>
<p>With special fights you can choose the type of fight, but handicaps and the fight set-up are chosen for you. There are no additional handicaps here and you cannot take part in a challenge until you have faced the bosses involved in a climber fight.</p>
<p>The fights are as follows:</p>
<ul>
<li><u>Revenge of the Evil Chickens</u><br>Fend off 15 evil chickens.</li>
<li><u>Finish Him!</u><br>Defeat Arrav, Koschei the Deathless, General Khazard, Black Knight Guardian, the Kendal, and the Black, Grey and White golems. They start at 20% health.</li>
<li><u>Just Die Already</u><br>Defeat 6 Dagannoth sentinels.</li>
<li><u>No More Nomadâ€¦ No More!</u><br>Face Nomad twice in a row.</li>
<li><u>I Eat Dagannoths For Breakfast</u><br>Fight both Dagannoth mothers and 2 Dagannoth sentinels.</li>
<li><u>Indigestion</u><br>Defeat the end bosses from <a href="recipe_for_disaster_members">Recipe for Disaster</a>.</li>
<li><u>You'll Never Defeat Me!</u><br>Survive for 5 minutes in an arena with the Inadequacy, the Everlasting, the Illusive and the Untouchable.</li>
<li><u>Hey! Eat Power-up</u><br>Defeat all the class H and class G monsters using smart bomb power ups that spawn around the arena.</li>
</ul>
<p><span class="title">Spectating</span></p>
<p>Using the Dominion Medallion (available from the Tower face) you can tag another player. Should this player fight in the Dominion Tower, you will be able to watch them from one of the spectator points along the edge of the arena. The other player must have 'accept aid' turned on.</p>
<p>To observe your tagged player, click on the stairs leading up from the ground floor and choose "spectate".</p>
<p>You can leave the arena at any time through one of the sets of stairs.</p>
