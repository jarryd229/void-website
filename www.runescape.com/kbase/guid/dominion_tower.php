<br class="clear"><a name="tower"></a><div class="headings">
<span class="headertitleleft"><span class="headertitleright">The Tower</span></span>
</div>
<p>To start fighting in the tower, click on one of the flights of stairs leading up from the ground floor. You will get a choice of which game mode you want to play.</p>
<p>The modes are:</p>
<ul>
<li>Climber</li>
<li>Endurance</li>
<li>Special</li>
<li>Freestyle</li>
</ul>
<p>You can also spectate if you wish.</p>
