<div class="headings">
<span class="headertitleleft"><span class="headertitleright">Recommended Items</span></span>
</div>
<br>
We recommend that you bring a good choice of armour and weaponry to protect you from any opponent. Also, a good source of potions will aid you in difficult situations. If you've bought any of the Castle Wars rewards, you may wish to bring those, as some have effects that will prove beneficial during a match.
<br><br>
You may also be tempted to bring a <a href="magic_non_combat_magic">Castle Wars bracelet</a>, which increases the potency of bandages used during a match.
<br><br>
<i>Note: Non-combat items are not allowed in the arena, e.g. food.</i>
