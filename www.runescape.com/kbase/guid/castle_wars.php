<a name="one"></a><sup>1</sup>Wearing the basic platebody, platelegs and shield during a Castle Wars match gives you +10% damage against the enemy team.<br><br>
<a name="two"></a><sup>2</sup>Wearing the detailed platebody, platelegs and shield during a Castle Wars match gives you +15% damage against the enemy team, and +10% damage against barricades/ballistas.<br><br>
<a name="three"></a><sup>3</sup>Wearing the intricate platebody, platelegs and shield during a Castle Wars match gives you +20% damage against the enemy team, +10% damage against barricades/ballistas, and immunity from catapult damage.<br><br>
<a name="four"></a><sup>4</sup>Wearing the profound platebody, platelegs and shield during a Castle Wars match gives you +25% damage against the enemy team, +10% damage against barricades/ballistas, and immunity from catapult/ballista damage.<br><br>
<a name="asterix"></a><sup>*</sup>Guthix halo reduces the drain rate of the prayers Clarity of Thought, Improved Reflexes, Incredible Reflexes, Chivalry, Piety; Saradomin halo reduces the drain rate of Thick Skin, Rock Skin, Steel Skin, Chivalry, Piety; Zamorak halo reduces the drain rate of Burst of Strength, Superhuman Strength, Ultimate Strength, Chivalry, Piety.<br><br>
<a name="tilde"></a><sup>~</sup>Each cape can only be purchased after: getting the most flag captures in a match (flag cape); getting the most kills in a match (kills cape); completing 500 matches (hobbyist cape); completing 1,000 matches (enthusiast cape); and completing 5,000 matches (professional cape).<br><br>
<a name="hash"></a><sup>#</sup>Before you can use a ballista in a Castle Wars match, you must purchase all four ballista parts and at least one bit of ammo for it. You can take up to 100 bits of ammo with you into a match.<br><br>
<a name="plus"></a><sup>+</sup>Melee potion set contains 1x Super Attack, 1x Super Strength, 1x Super Defence, 1x Super Energy.<br>Ranged potion set contains 1x Ranging potion, 1x Super Defence, 1x Super Energy.<br>Magic potion set contains 1x Magic Essence, 1x Super Defence, 1x Super Energy.<br>The potions in these sets may only be used during a Castle Wars match.<br><br>
<a name="and"></a><sup>&amp;</sup>Chinchompas can be deployed in timer or proximity mode. Proximity mode allows the chincompa to look for players on the opposite team exploding when it finds them. The damage affects any player within the area and will not give experience for the damage caused. In timer mode the chinchompa will run around until its timer expires at which point it will explode. You may only hold 3 chinchompas on you and only 10 chinchompas in total can be deployed at any one time (by yourself or your team). Chinchompas may only be used on the ground floor of Castle Wars.<br><br>
<a name="circumflex"></a><sup>^</sup>There is a chance for this item to drop. Also, should the item drop, you will get to pick if you would like the Trickster, Vanguard or Battlemage type. See <a href="hybrid_armour_members">hybrid armour</a> for more details.
