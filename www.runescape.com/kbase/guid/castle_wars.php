The location of Castle Wars
</div>

The Castle Wars Arena is located west of Yanille.
<br><br>
The fastest way to get to Castle Wars is to use a <a href="magic_non_combat_magic">ring of duelling</a>, which will teleport you directly there. If you have access to the fairy ring network, you can travel to BKP, which is just south of Castle Wars. Various teleport spells will get you within walking distance, such as <a href="magic_teleportation_spells">Ardougne Teleport</a> and <a href="magic_teleportation_spells">Watchtower Teleport</a>.
