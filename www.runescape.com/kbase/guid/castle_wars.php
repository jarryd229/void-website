The starting room
</div>

When the match begins (and if you're killed during the match), you'll find yourself in your castle's starting room. In this room is a table of bandages, which you should definitely grab a handful of. As you are unable to bring food with you into a Castle Wars match, these are the only means by which you have to heal yourself (or your teammates). Bandages heal up to 100 life points, restore 30% of your run energy and cure any poisons that you are suffering from. There's also a tinderbox in this room, which you may find useful. <br><br>
Don't linger in the starting room for too long, though, as if you haven't left within the first two minutes of the match you'll be dumped back into your team's waiting area for the next match to begin. Also, note that only members of your team are able to enter this starting room; likewise, you will not be able to get into your opponents' starting room.
