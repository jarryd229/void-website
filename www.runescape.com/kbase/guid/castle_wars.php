Castle Wars waiting area
</div>

Once you have made your way to the entrance of Castle Wars, you can either speak to Lanthus and he will explain the rules, or you can obtain a Castle Wars manual for free from the rewards store.
<br><br>
To join a match, choose which team you would like to be on and enter a portal. Entering the red portal on the left will enrol you on the Zamorak team, and entering the blue portal on the right will enrol you on the Saradomin team. The green portal in the middle is the Guthix portal; if you choose to go through this portal you will be enrolled on whichever team currently has the <b>fewest</b> players, be that Saradomin or Zamorak. Once you have entered a portal, you will be taken to that team's waiting area. <br><br class="clear"> <img class="imgleft" style="width: 150px; height: 150px" src="<?php echo WWW; ?>/img/main/kbase/minigames/castlewars/castle_wars_sheep.gif" alt="[image]">
If you enter a portal wearing an item that represents one of the other gods (e.g. entering the Zamorak portal wearing an item of Saradomin), then you will change form and remain that way until the game starts. Zamorak punishes you by turning you into an imp; Saradomin gives you some time to reflect on things as a rabbit; Guthix makes you one with nature as a, er, sheep. Don't worry, though: you'll only remain in this form until either the match begins or you use the exit portal.</i>
<br><br>
If there is a game in progress, you may observe the action from the <b>viewing orb</b> near Lanthus, which will allow you to survey the battlefield from a variety of viewpoints. Castle Wars matches last for twenty minutes, with a five minute interval between each one. It is in the five minutes between matches that you will go to the waiting area. A message will be shown at the top of the screen indicating the length of time remaining to the start of the next match.
