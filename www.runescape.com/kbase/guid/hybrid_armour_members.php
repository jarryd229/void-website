Hybrid armour
</div>

<p>Hybrid armour can be obtained from various minigames. These pieces of armour are entirely cosmetic except for when they are in the following PvP minigames:</p>
<ul>
<li><a href="castle_wars">Castle Wars</a></li>
<li><a href="soul_wars">Soul Wars</a></li>
<li><a href="fist_of_guthix">Fist of Guthix</a></li>
<li><a href="tzhaar_fight_pit">TzHaar Fight Pits</a></li>
</ul>
<p>When playing in the above minigames, you will deal additional damage against other players. Note that in Castle Wars this damage bonus does not stack with the Castle Wars armour sets. Whichever set provides the biggest damage boost will be the set that provides the bonus. </p>
<p>Hybrid armour is untradeable. </p>
