Clan Wars location
</div>

The Clan Wars arena is located in the eastern <a href="wilderness">Wilderness</a>, north-east of the Chaos Temple and east of the volcano.
<br><br>
To get to the Clan Wars challenge hall, you will need to walk north from Varrock and around the fiery lava that surrounds the Chaos Temple. Walk north-east from there and you should find it quickly.
<br><br>
If you have access to <a href="magic_the_ancient_magicks">Ancient Magicks</a>, you will find that the Carrallangar teleport drops you conveniently at the volcano. It's a fairly short run across a stretch of the Wilderness to then get to the Clan Wars challenge hall. Be aware, though, that you might just run into some nasty creatures while you do so.
<br><br>
Simpler still, you can use a games necklace to teleport directly to the Clan Wars challenge hall.
<br><br>
The challenge hall itself does not count as part of the Wilderness, so the easiest way to leave is to simply teleport out to some safer location. For this reason, it's probably a good idea to bring some method of teleporting.
