If you are not a member of a clan chat channel, or simply want a bit of solo chaos, then the free-for-all arenas may be the challenge you're looking for. The arenas can be accessed through the white or red portals in the north of the challenge hall. <br><br>
The white portal will take you to an arena of endless battle, in which you will not lose any items if you die. The red portal will take you to a different arena, but you <b>will lose your items if you die</b>. There are no gravestones in the dangerous arena, so it is probably safer to only take items you are not especially attached to.
<br><br>
In the dangerous free-for-all arena, you may be able to collect some of your defeated foes' items - food, potions, runes and other ammunition. Any such drops will affect your trade limit, as well as your victim's trade limit.
<br><br>
Both free-for-all arenas have a 'non-combat zone' at the southern end of the arena, so you will be able to prepare yourself when you arrive.
