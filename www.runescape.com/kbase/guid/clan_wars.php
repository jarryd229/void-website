<br>
If you wish to initiate a clan war or accept a clan war challenge, you must be ranked as a captain or higher in your <a href="controls_clan_chat">clan chat channel</a>.
<br><br>
If you are not a member of a clan, or simply want to take part in a grand battle, you can enter one of the free-for-all arenas and enjoy an endless battle.
<br class="clear">
<a name="recommended_items"></a>
<br>
<div class="headings">
<span class="headertitleleft"><span class="headertitleright">Recommended Items</span></span>
</div>
<br>
<img class="imgleft" style="width: 150px; height: 150px" src="<?php echo WWW; ?>/img/main/kbase/minigames/clanwars/cool_warrior.gif" alt="[image: Snipe prime targets]">
Probably the most useful piece of equipment will be a <a href="cape_guide">wilderness cape</a> so you will know your friends from your enemies with ease. You should try to make sure everyone in your clan has the same cape to make identification simpler. Conveniently enough, one of the Wilderness cape sellers, Larry, has come to enjoy the comfort of the Clan Wars challenge hall, so it's a simple matter to purchase one from him.
<br><br>
Since you are representing the pride and power of your clan, and to give yourself and your clan the best chance of winning, it is useful to field the best arms and armour that you can.
<br><br>
You should try to pick out a role for yourself, where you can perform useful functions for your clan. What works best for you and your clan is something you'll find out as you fight in more clan wars.
