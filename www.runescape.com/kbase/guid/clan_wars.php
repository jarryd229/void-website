If you are inside the challenge hall and have a <a href="controls_clan_chat">clan</a> rank of at least Captain in an active clan chat channel, you will have a new "Challenge" option when right-clicking on a member of another clan. You are able to select "Challenge" in order to declare your intention of starting a clan war, in the same way as challenging when duelling. If the person you challenge is also a Captain (or higher) in an active clan chat channel, they will have the option to accept.
<br><br>
<i>To accept the clan war</i><br>
If you are of a suitable rank in an active clan chat channel and a dastardly foe of suitable rank challenges you to a clan war, you will receive a message stating this in your chat window.
<br><br>
At this point, you can click on the challenge in your chat or right-click on the other player and select 'Challenge' to accept, or simply ignore it and wait for another challenge.
